public class Data_Types {
    public static void main(String[] args){
        byte x = 10;
        int y = 6;
        short z = 12;
        int a = y + z; // int + short = int
        float b = 6.54f + x; // float + int = float

        // float + double = double

        System.out.println(a); // int
        System.out.println(b); // float
    }
}
